#include <unistd.h>

int main(void)
{
	int n;
	int i;
	
	n = 2;
	i = 8;
	while(i > 0)
	{
		if(n & (1 << i))
		{
			write(1, "1", 1);
		}
		else
		{
			write(1, "0", 1);
		}
		i--;
	}
	write(1, "\n", 1);
	return(0);
}
