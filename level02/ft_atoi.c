int ft_atoi(const char *str)
{
	int i;
	int sign;
	int res;

	i = 0;
	if(str == '\0')
		return(0);
	while(str[i] == '\t' || str[i] == '\v' || str[i] == '\n' || str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if(str[i] == '-')
	{
		sign = -1;
		i++;
	}
	while(str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	return(res * sign);
}
